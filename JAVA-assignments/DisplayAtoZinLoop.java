import java.util.Scanner;

public class DisplayAtoZinLoop {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("Upper Case Letter:");
		for(char c='a';c <='z';c++) {
			System.out.print(c +" ");
		}
		
		System.out.println("\nLower Case Letter:");
		for(char c='a';c <='z';c++) {
			System.out.print(c +" ");
		}


	}

}
