package javabasics;

public class Datatypes {

	public static void main(String[] args) {
		int age=24;//32bit
		String name="hari";// string is the non primitive datatype which has larger size than all primitive DT
		double totalMoney= 100.5;//64bit
		float money= 100.5f;//32 bit
		char Money='1';//16bit
		System.out.println(name);
		System.out.println(age);
		System.out.println(totalMoney);
		System.out.println(money);
		System.out.println(Money);
		
		
		

	}

}
